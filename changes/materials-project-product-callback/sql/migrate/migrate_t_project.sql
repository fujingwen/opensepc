    id,
    tenant_id,
    construction_permit,
    permit_issue_date,
    project_name,
    project_nature,
    building_area,
    project_progress,
    project_address,
    structure_type,
    quality_supervision_agency,
    construction_unit,
    construction_unit_manager,
    manager_contact,
    has_report,
    is_integrated,
    remarks,
    create_by,
